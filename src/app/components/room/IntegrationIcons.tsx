/** Map platform slug → display metadata */
export const INTEGRATION_CONFIG: Record<string, {
  label: string;
  description: string;
  color: string;
  bgColor: string;
  borderColor: string;
  patterns: RegExp[];
}> = {
  github: {
    label: 'GitHub',
    description: 'Repository',
    color: 'text-white',
    bgColor: 'bg-[#24292e]',
    borderColor: 'border-white/20',
    patterns: [/github\.com\/[\w-]+\/[\w.-]+/i],
  },
  figma: {
    label: 'Figma',
    description: 'Design file',
    color: 'text-white',
    bgColor: 'bg-[#1e1e1e]',
    borderColor: 'border-[#A259FF]/40',
    patterns: [/figma\.com\/(file|design|proto)\//i],
  },
  notion: {
    label: 'Notion',
    description: 'Document',
    color: 'text-white',
    bgColor: 'bg-[#1e1e1e]',
    borderColor: 'border-white/20',
    patterns: [/notion\.so\//i, /notion\.site\//i],
  },
  linear: {
    label: 'Linear',
    description: 'Project',
    color: 'text-white',
    bgColor: 'bg-[#1e1e1e]',
    borderColor: 'border-[#5E6AD2]/40',
    patterns: [/linear\.app\//i],
  },
  miro: {
    label: 'Miro',
    description: 'Board',
    color: 'text-white',
    bgColor: 'bg-[#FFD02F]',
    borderColor: 'border-[#FFD02F]/40',
    patterns: [/miro\.com\/app\//i],
  },
};

export function getPlatformFromUrl(url: string): string | null {
  for (const [platform, config] of Object.entries(INTEGRATION_CONFIG)) {
    if (config.patterns.some(p => p.test(url))) return platform;
  }
  return null;
}

export function IntegrationPlatformIcon({
  platform,
  className,
}: {
  platform: string;
  className?: string;
}) {
  switch (platform) {
    case 'github':  return <GitHubIcon className={className} />;
    case 'figma':   return <FigmaIcon className={className} />;
    case 'notion':  return <NotionIcon className={className} />;
    case 'linear':  return <LinearIcon className={className} />;
    case 'miro':    return <MiroIcon className={className} />;
    default: return null;
  }
}
